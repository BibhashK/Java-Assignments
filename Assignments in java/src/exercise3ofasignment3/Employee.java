package exercise3ofasignment3;

public class Employee {

	String Name;
	int empid;
	double Salary;
	
	
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getEmpid() {
		
		return empid;
	}
	public void setEmpid(int empid) {
		this.empid = empid;
	}
	public double getSalary() {
		
		return Salary;
	}
	public void setSalary(double salary) {
		Salary = salary;
	}
	
}
